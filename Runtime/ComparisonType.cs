namespace CustomAttributes
{
    /// <summary>
    /// Types of comparison that can be performed
    /// </summary>
    public enum ComparisonType
    {
        /// <summary>
        /// Equals comparison (==)
        /// </summary>
        Equals,
        
        /// <summary>
        /// Not equals comparison (!=)
        /// </summary>
        NotEquals,
        
        /// <summary>
        /// Greater than comparison (>)
        /// </summary>
        GreaterThan,
        
        /// <summary>
        /// Greater than or equal comparison (>=)
        /// </summary>
        GreaterThanOrEqual,
        
        /// <summary>
        /// Less than comparison (<)
        /// </summary>
        LessThan,
        
        /// <summary>
        /// Less than or equal comparison (<=)
        /// </summary>
        LessThanOrEqual
    }
}